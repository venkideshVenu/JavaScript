// Introduction to Functional Programming in JavaScript

/*
Functional programming is a programming paradigm where programs are constructed by applying and composing functions.
It emphasizes the use of pure functions, immutability, and avoiding side effects.
JavaScript supports functional programming concepts and allows writing code in a functional style.
*/

// Example of a simple pure function
function add(a, b) {
  return a + b;
}

console.log("Add 2 + 3 =", add(2, 3));

// Explanation:
// - Functions are treated as first-class citizens.
// - Functions can be passed as arguments, returned from other functions, and assigned to variables.
